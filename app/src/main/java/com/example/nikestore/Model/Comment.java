package com.example.nikestore.Model;

public class Comment {
    Author author;
    String content;
    String date;
    int id;
    String title;

    public Author getAuthor() {
        return author;
    }

    public String getContent() {
        return content;
    }

    public String getDate() {
        return date;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }
}
